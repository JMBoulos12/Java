


/*
  Create a function that returns true when x is equal to y; otherwise return false.

  Examples
  isSameNum(4, 8) ➞ false

  isSameNum(2, 2) ➞  true

  isSameNum(42, 32) ➞  false
  Notes
  Don't forget to return the result.
  
  19-January-2023
*/

public class Program {
  public static boolean isSameNum(int x, int y) {
    return x == y;
  }
}
